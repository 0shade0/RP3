        private PictureBox[,] squares = new PictureBox[36, 28];

        public Grid(Form form, Point squareSize)
        {
            for (int i = 0; i < 36; i++)
            {
                for (int j = 0; j < 28; j++)
                {
                    squares[i, j] = new PictureBox();
                    squares[i, j].Size = new Size(squareSize.X, squareSize.Y);
                    squares[i, j].Location = new Point(j * squareSize.X, i * squareSize.Y);
                    if (map[i, j] == '#')
                        squares[i, j].BackColor = Color.DarkBlue;
                    
                    form.Controls.Add(squares[i, j]);
                }
            }
        }

        public char getSquareValue(int i, int j)
        {
            return map[i, j];
